<h2>Admin Users Page Help</h2>
<h3>Granting Access to Admin</h3>
<p> The Users List is a record of all usernames and passwords that grant access to the Administration area. You can easily add new users and modify or delete existing users as needed.</p>
<h3>Security Note:</h3>
<p>Be sure to delete the default record of admin/admin before your store goes live. </p>
