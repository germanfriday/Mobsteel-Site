<h3>Order Search by Date</h3>
<p> Here you are able to pull a list of Pending, Verified, Shipped, Canceled, or Returned orders depending on the link you select under the Orders tab on the admin menu, and search for orders by date. </p>
<p>The results of your search are displayed in a table sorted by date. Clicking on the view icon will take you to the <a href="<?php echo($cartweaver->thisPage);?>?helpFileName=OrderDetails.php">Orders Details</a> page. </p>
