<h2>Product Image Upload Admin Help</h2>
<h3>Upload Image Files </h3>
<p> This page is linked to from the Product Admin Page. Here you can select the type of product image you wish to upload, a thumbnail or a large image, and you can browse to the image file on your system and select it to be uploaded. </p>
<p>The image upload will only allow web compatible image files to be uploaded. Those image files ending in .gif, .jpg, .jpeg or .png.</p>
<p>Once the Image is selected and uploaded the appropriate image name field will be updated with the name of the image file that was uploaded. </p>
